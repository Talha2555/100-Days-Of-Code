/*Find the Length of a String: Write a function that takes a string as an 
argument and returns the number of characters in the string.*/

//This function counts how many characters are in a string

function strLength(str: string): number {
  return str.length; //Return the numbers of characters in the string
}
//Example :Measuring the length of a name
console.log(strLength("TalhaKhan")); //Output 9
